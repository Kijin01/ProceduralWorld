int AVoxelChunk::ConvertPointsToTrigoIndex(TArray<FPointFNG> Points, int FloorValue) const {
    int     result;
    int     index;

    result = 0;
    index = 0;
    while (index < 8)
    {
        if (Points[index].Value < FloorValue)
            result |= 1 << index;
        index++;
    }
    return (result);
}

int AVoxelChunk::GetPointDensityFromLocation(FVector PointLocation, FVector ActorLocation) const {
    int     Result;
    float   NoiseValue;
    FVector WorldPointLocation;

    Result = 0;
    WorldPointLocation = PointLocation + ActorLocation;
    if (chunk_settings.DensityType == Density::Floor)
    {
        Result = WorldPointLocation.Z;
        NoiseValue = _Noise->GetNoise2D(WorldPointLocation.X, WorldPointLocation.Y);
        Result += NoiseValue * (float)noise_settings.Amplitude;
    }
    else if (chunk_settings.DensityType == Density::Cave)
    {
        NoiseValue = _Noise->GetNoise3D(WorldPointLocation.X, WorldPointLocation.Y, WorldPointLocation.Z);
        Result = NoiseValue * (float)noise_settings.Amplitude;
    }
    else if (chunk_settings.DensityType == Density::Floor3D)
    {
        Result = WorldPointLocation.Z;
        NoiseValue = _Noise->GetNoise3D(WorldPointLocation.X, WorldPointLocation.Y, WorldPointLocation.Z);
        Result += NoiseValue * (float)noise_settings.Amplitude;
    }
    else
        Result = chunk_settings.FloorValue;
    return (Result);
}

int AVoxelChunk::GetPointValue(FVector PointLocation, FVector ActorLocation) const {
    int result;

    if (!_PointDensityMap.Contains(PointLocation))
    {
        result = GetPointDensityFromLocation(PointLocation, ActorLocation);
        //_PointDensityMap;
    }
    else
        result = *_PointDensityMap.Find(PointLocation);
    return (result);
}

