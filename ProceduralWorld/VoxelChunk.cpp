void AVoxelChunk::StartGeneration() {

    _IsGenerated = false;
    FPointFNG         NewPoint;
    TArray<FPointFNG> pointsArray;
    float           ChunkRay;
    FVector         ChunkLocation;
    int             NumberOfPointIn1Axis;
    float           DistanceBetweenPoints;
    FVector         ChunkCorner;

    ChunkLocation = this->GetActorLocation();

    NumberOfPointIn1Axis = chunk_settings.Scale;
    DistanceBetweenPoints = chunk_settings.PointOffset;
    ChunkRay = (NumberOfPointIn1Axis - 1) * DistanceBetweenPoints / 2;

    // If you set this value to 0 it will generate into a localspace 
    ChunkCorner = FVector(0, 0, 0);//FVector(ChunkLocation.X - ChunkRay, ChunkLocation.Y - ChunkRay, ChunkLocation.Z - ChunkRay);
    for (float z = 0; z <= ChunkRay * 2; z += DistanceBetweenPoints)
    {
        for (float y = 0; y <= ChunkRay * 2; y += DistanceBetweenPoints)
        {
            for (float x = 0; x <= ChunkRay * 2; x += DistanceBetweenPoints)
            {
                NewPoint.Pos = FVector(ChunkCorner.X + x, ChunkCorner.Y + y, ChunkCorner.Z + z);
                NewPoint.Value = GetPointValue(NewPoint.Pos, ChunkLocation);
                pointsArray.Add(NewPoint);
            }
        }
    }
    FillBuffer(pointsArray, NumberOfPointIn1Axis);
    _IsGenerated = true;
}

void AVoxelChunk::FillBuffer(TArray<FPointFNG> pointsArray, int NumberOfPointIn1Axis) {
    TArray<FPointFNG> pointsB;

    int Index;
    int IndexEnd;
    int TrigoIndex;
    int Index2;
    int TriangleIndex;
    TriangleIndex = 0;
    Index = 0;
    IndexEnd = (NumberOfPointIn1Axis - 1) * (NumberOfPointIn1Axis - 1) * (NumberOfPointIn1Axis - 1);

    while (Index < IndexEnd)
    {
        // Without Octree you cannot use leafnode to find the cube you are currently looking into
        // so you have to get the 8 point manually like this:
        pointsB.Add(pointsArray[Index]);
        pointsB.Add(pointsArray[Index + 1]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis + 1]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis * NumberOfPointIn1Axis]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis * NumberOfPointIn1Axis + 1]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis * NumberOfPointIn1Axis + NumberOfPointIn1Axis]);
        pointsB.Add(pointsArray[Index + NumberOfPointIn1Axis * NumberOfPointIn1Axis + NumberOfPointIn1Axis + 1]);

        TrigoIndex = ConvertPointsToTrigoIndex(pointsB, chunk_settings.FloorValue);
        Index2 = 0;
        while (Index2 < 16 && AVoxelChunk::trigoTable[TrigoIndex][Index2] != -1)
        {
            _ProceduralMeshBuffer.Vertices.Add(GetLocationOfTrigoPoint(AVoxelChunk::trigoTable[TrigoIndex][Index2], pointsB));
            _ProceduralMeshBuffer.Vertices.Add(GetLocationOfTrigoPoint(AVoxelChunk::trigoTable[TrigoIndex][Index2 + 1], pointsB));
            _ProceduralMeshBuffer.Vertices.Add(GetLocationOfTrigoPoint(AVoxelChunk::trigoTable[TrigoIndex][Index2 + 2], pointsB));

            _ProceduralMeshBuffer.Triangles.Add(TriangleIndex++);
            _ProceduralMeshBuffer.Triangles.Add(TriangleIndex++);
            _ProceduralMeshBuffer.Triangles.Add(TriangleIndex++);

            _ProceduralMeshBuffer.VertexColors.Add(FLinearColor(0.0f, 0.0f, 1.0f));
            _ProceduralMeshBuffer.VertexColors.Add(FLinearColor(1.0f, 0.0f, 0.0f));
            _ProceduralMeshBuffer.VertexColors.Add(FLinearColor(0.0f, 1.0f, 0.0f));

            _ProceduralMeshBuffer.Uvs.Add(FVector2D(0, 0));
            _ProceduralMeshBuffer.Uvs.Add(FVector2D(1, 0));
            _ProceduralMeshBuffer.Uvs.Add(FVector2D(1, 1));

            Index2 += 3;
        }
        pointsB.Empty();
        Index++;
    }  
}


